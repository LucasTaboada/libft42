#include "libft.h"

int			ft_num_len(int nbr)
{
	size_t	size;

	if (nbr == 0)
	{
		return (1);
	}
	size = 0;
	if (nbr < 0)
	{
		size++;
	}
	while (nbr)
	{
		nbr /= 10;
		size++;
	}
	return (size);
}
